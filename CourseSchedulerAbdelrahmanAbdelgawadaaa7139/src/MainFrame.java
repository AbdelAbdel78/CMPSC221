    private void dropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseButtonActionPerformed
        // TODO add your handling code here:
        CourseEntry tempC = CourseEntry.class.cast(dropCourseComboBox.getSelectedItem());
        dropCourseTextArea.setText("");
        ArrayList<ScheduleEntry> sched = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, tempC.getCourseCode());
        ArrayList<ScheduleEntry> waited = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, tempC.getCourseCode());
        dropCourseTextArea.append("\nScheduled students dropped from the course:");
        for (ScheduleEntry entry: sched) {
            dropCourseTextArea.append("\n" + StudentQueries.getStudent(entry.getStudentID()));
        }
        dropCourseTextArea.append("\nWaitlisted students dropped from the course:");
        for (ScheduleEntry entry: waited) {
            dropCourseTextArea.append("\n" + StudentQueries.getStudent(entry.getStudentID()));
        }
        ScheduleQueries.dropScheduleByCourse(currentSemester, tempC.getCourseCode());
        CourseQueries.dropCourse(currentSemester, tempC.getCourseCode());
        
        updateCourseComboBoxes();
    }//GEN-LAST:event_dropCourseButtonActionPerformed

