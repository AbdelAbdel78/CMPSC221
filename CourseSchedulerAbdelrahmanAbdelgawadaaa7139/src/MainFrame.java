    private void updateCourseComboBoxes() {
        courses = CourseQueries.getAllCourses(currentSemester);
        scheduleCoursesCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        studentDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        dropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        courseListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
        updateTables();
    }
    
    private void updateStudentComboBoxes() {
        students = StudentQueries.getAllStudents();
        scheduleCoursesStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        dropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        displayScheduleComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        studentDropCourseComboBox2.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        
        updateTables();
    }
    
    private void updateTables() {
        updateDisplayCoursesTable();
        updateScheduleTable();
        updateCourseListSchedTable();
        updateCourseListWaitTable();
    }
    
    private void updateDisplayCoursesTable() {
        DefaultTableModel displayCoursesTableModel = (DefaultTableModel)displayCoursesTable.getModel();
        displayCoursesTableModel.setNumRows(0);
        
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        Object [] rowData = new Object[3];
        for (CourseEntry course: courses) {
            rowData[0] = course.getCourseCode();
            rowData[1] = course.getCourseDescription();
            rowData[2] = course.getSeats();
            displayCoursesTableModel.addRow(rowData);
        }
    }
    
    private void updateScheduleTable() {
        DefaultTableModel displayScheduleTableModel = (DefaultTableModel)displayScheduleTable.getModel();
        displayScheduleTableModel.setNumRows(0);
        
        StudentEntry tempS = StudentEntry.class.cast(displayScheduleComboBox.getSelectedItem());
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, tempS.getStudentID());
        
        Object [] rowData = new Object[2];
        for (ScheduleEntry entry: schedule) {
            rowData[0] = entry.getCourseCode();
            rowData[1] = entry.getStatus();
            displayScheduleTableModel.addRow(rowData);
        }
    }
    
    private void updateCourseListSchedTable() {
        DefaultTableModel courseListScheduledTableModel = (DefaultTableModel)courseListScheduledTable.getModel();
        courseListScheduledTableModel.setNumRows(0);
        
        CourseEntry tempC = CourseEntry.class.cast(courseListComboBox.getSelectedItem());
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, tempC.getCourseCode());
        
        Object [] rowData = new Object[3];
        for (ScheduleEntry entry: schedule) {
            StudentEntry tempS = StudentQueries.getStudent(entry.getStudentID());
            rowData[0] = tempS.getLastName();
            rowData[1] = tempS.getFirstName();
            rowData[2] = tempS.getStudentID();
            courseListScheduledTableModel.addRow(rowData);
        }
    }
    
    private void updateCourseListWaitTable() {
        DefaultTableModel courseListWaitlistedTableModel = (DefaultTableModel)courseListWaitlistedTable.getModel();
        courseListWaitlistedTableModel.setNumRows(0);
        
        CourseEntry tempC = CourseEntry.class.cast(courseListComboBox.getSelectedItem());
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, tempC.getCourseCode());
        
        Object [] rowData = new Object[3];
        for (ScheduleEntry entry: schedule) {
            StudentEntry tempS = StudentQueries.getStudent(entry.getStudentID());
            rowData[0] = tempS.getLastName();
            rowData[1] = tempS.getFirstName();
            rowData[2] = tempS.getStudentID();
            courseListWaitlistedTableModel.addRow(rowData);
        }
    }
