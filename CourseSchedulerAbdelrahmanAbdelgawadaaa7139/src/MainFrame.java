    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry tempS = StudentEntry.class.cast(dropStudentComboBox.getSelectedItem());
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, tempS.getStudentID());
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        dropStudentTextArea.setText(tempS + " has been dropped from the list of students\n");
        
        for (String semester: semesters) {
            dropStudentTextArea.append("\nFor semester: " + semester);
            for (ScheduleEntry entry: schedule) {
                ScheduleQueries.dropStudentScheduleByCourse(semester, tempS.getStudentID(), entry.getCourseCode());
                if ("S".equals(entry.getStatus())) {
                dropStudentTextArea.append("\n"+ tempS + " has been dropped from " + entry.getCourseCode());
                ArrayList<ScheduleEntry> entrys = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, entry.getCourseCode());
                    if (entrys.size() > 0) {
                        ScheduleQueries.updateScheduleEntry(currentSemester, entrys.get(0));
                        dropStudentTextArea.append("\n" + StudentQueries.getStudent(entrys.get(0).getStudentID()) + " has been scheduled for " + entry.getCourseCode());
                    }
                } else if ("W".equals(entry.getStatus())) {
                    dropStudentTextArea.append("\n"+ tempS + " has been dropped from the waitlist for " + entry.getCourseCode());
                }
            }
        }
        StudentQueries.dropStudent(tempS.getStudentID());
        
        updateStudentComboBoxes();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

