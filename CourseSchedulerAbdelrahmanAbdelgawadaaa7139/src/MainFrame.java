    private void scheduleCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesButtonActionPerformed
        CourseEntry tempC = CourseEntry.class.cast(scheduleCoursesCourseComboBox.getSelectedItem());
        StudentEntry tempS = StudentEntry.class.cast(scheduleCoursesStudentComboBox.getSelectedItem());
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        int enrolledSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, tempC.getCourseCode());
        int seatsOpen = CourseQueries.getCourseSeats(currentSemester, tempC.getCourseCode());
        
        if (enrolledSeats < seatsOpen) {
            ScheduleEntry student = new ScheduleEntry(currentSemester, tempC.getCourseCode(), tempS.getStudentID(), "S", currentTimestamp);
            ScheduleQueries.addScheduleEntry(student);
            scheduleCoursesStatusLabel.setText(scheduleCoursesStudentComboBox.getSelectedItem().toString() + " has been scheduled for the course " + scheduleCoursesCourseComboBox.getSelectedItem().toString());
        } else {
            ScheduleEntry student = new ScheduleEntry(currentSemester, tempC.getCourseCode(), tempS.getStudentID(), "W", currentTimestamp);
            ScheduleQueries.addScheduleEntry(student);
            scheduleCoursesStatusLabel.setText(scheduleCoursesStudentComboBox.getSelectedItem().toString() + " has been waitlisted for the course " + scheduleCoursesCourseComboBox.getSelectedItem().toString());
        }
        
        updateTables();
    }//GEN-LAST:event_scheduleCoursesButtonActionPerformed

